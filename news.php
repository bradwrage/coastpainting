			<p>Circuit Therapeutics and Boehringer Ingelheim announce signature of a research partnership agreement to discover new approaches to treating psychiatric disorders<p>

<p>MENLO PARK, California and INGELHEIM, Germany, December 18th, 2013 -
Boehringer Ingelheim and Circuit Therapeutics have entered into a research collaboration agreement for the discovery of new ways of treating psychiatric disorders. Circuit Therapeutics provides an innovative platform that allows the identification and characterization of drug targets from brain circuits involved in normal and pathological behaviors. Circuit Therapeutics’ key technology, optogenetics, allows the control of individual nerve cells and circuits within the brain enabling their functional identification and characterization. The combination of optogenetics and sophisticated molecular technologies provides an unprecedented level of focus to identify novel drug targets and approaches for drug development. The new insights enabled by circuit-based analysis provide fundamentally differentiated approaches to understanding and treating brain disorders, which have otherwise been difficult to address.</p>
<div class="more-news-link"><a  data-expand="more-news" href="">Read More</a></div><br />

<div data-expand-for="more-news">
<p>Under the terms of the agreement the research and development capabilities of the two companies will be brought together to provide novel avenues for drug discovery in a three-year research program. Further details of the agreement are not disclosed.</p>

<p>“We are excited to work with Boehringer Ingelheim to utilize Circuit’s unique capabilities to advance drug discovery. The innovative and ambitious vision and passion of Boehringer Ingelheim to make a difference in the area of neuropsychiatric disease treatments combined with Circuit’s innovative technologies makes an ideal partnership. We look forward to a highly productive research collaboration that will provide transformational drugs to impact patients’ lives,” said Fred Moll, Chairman of Circuit Therapeutics.</p>

<p>“We are very enthusiastic about being able to work in a partnership with Circuit Therapeutics, one of the leading organizations in the field of circuit biology and optogenetics”, said Dr. Bernd Sommer, the head of neuroscience research at Boehringer Ingelheim. “We believe that, by working together to investigate maladaptive brain circuitry with this exciting approach, we will be able to obtain a better understanding of the neurobiological basis of human neuropsychiatric disorders, thereby allowing us to make a difference to science, disease and patients.”</p>

<p>About Circuit Therapeutics
Circuit Therapeutics is a privately held discovery stage biotechnology company based in Menlo Park, California. The company’s cutting edge technologies focused on neural circuits are being applied to enable new ways of drug discovery and development of direct therapeutic interventions across a broad range of indications.</p>

<p>About Boehringer Ingelheim
The Boehringer Ingelheim group is one of the world’s 20 leading pharmaceutical companies. Headquartered in Ingelheim, Germany, it operates globally with 140 affiliates and more than 46,000 employees. Since it was founded in 1885, the family-owned company has been committed to researching, developing, manufacturing and marketing novel medications of high therapeutic value for human and veterinary medicine.</p>

<p>Social responsibility is a central element of Boehringer Ingelheim’s culture. Involvement in social projects, caring for employees and their families, and providing equal opportunities for all employees form the foundation of the global operations. Mutual cooperation and respect, as well as environmental protection and sustainability are intrinsic factors in all of Boehringer Ingelheim’s endeavors.</p>

<p>In 2012, Boehringer Ingelheim achieved net sales of about 14.7 billion euro. R&D expenditure in the business area Prescription Medicines corresponds to 22.5% of its net sales.</p>

